def sync_knowledge_from_files():
    """Синхронизирует файлы камней в таблицу knowledge для сайта и поиска."""
    from src.utils.text_loader import ContentLoader
    from src.database.db import db
    stones = ContentLoader.load_all_stones()
    if not stones:
        return
    with db.cursor() as c:
        for stone_id, data in stones.items():
            c.execute("""
                INSERT INTO knowledge
                    (stone_name, emoji, properties, chakra, short_desc, full_desc,
                     color, stone_id, tasks, price_per_bead, forms, notes)
                VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
                ON CONFLICT(stone_name) DO UPDATE SET
                    emoji=excluded.emoji,
                    properties=excluded.properties,
                    chakra=excluded.chakra,
                    short_desc=excluded.short_desc,
                    full_desc=excluded.full_desc,
                    color=excluded.color,
                    stone_id=excluded.stone_id,
                    tasks=excluded.tasks,
                    price_per_bead=excluded.price_per_bead
            """, (
                data.get('TITLE', stone_id),
                data.get('EMOJI', '💎'),
                data.get('PROPERTIES', ''),
                data.get('CHAKRA', ''),
                data.get('SHORT_DESC', ''),
                data.get('FULL_DESC', ''),
                data.get('COLOR', ''),
                stone_id,
                data.get('TASKS', ''),
                int(data.get('PRICE_PER_BEAD', 0) or 0),
                data.get('FORMS', ''),
                data.get('NOTES', '')
            ))
